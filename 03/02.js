function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function divide(a, b) {
    return a / b;
}

function multyply(a, b) {
    return a * b;
}

/* 
    Эти четыре функции чистые потому что, 
    возвращают одинаковый результат с одинаковыми аргументами, 
    без побочных эффектов и не используют глобальные переменные
*/    
